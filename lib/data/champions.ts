// Helper to find champion by name (case-insensitive)
export const findChampionByName = (name: string): Champion | undefined => {
    const lowerName = name.toLowerCase();
    return CHAMPIONS.find(
        (c) => c.name.toLowerCase() === lowerName || c.id.toLowerCase() === lowerName
    );
};

// Helper to get champion image URL by name or ID
export const getChampionImage = (nameOrId: string): string => {
    const champion = findChampionByName(nameOrId);
    if (champion) {
        return getChampionImageUrl(champion.id);
    }
    // Fallback: try to use the input directly as ID
    return getChampionImageUrl(nameOrId);
};
